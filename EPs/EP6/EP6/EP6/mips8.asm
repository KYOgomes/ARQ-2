# Associação
# x-> $s0
# y-> $s1
# z-> $s2

# $8 = 0x12345678; 
# $9=0x12; $10=0x34; $11=0x56; $12=0x78

# inicio
.text
.globl main

main:
ori  $t0, $zero, 0x1234  # t0 = 0x1234
sll  $t0,   $t0,     16  # t0 = 0x12340000
ori  $t0,   $t0, 0x5678  # t0 = 0x12345678

srl  $9, $8, 24     # 0x12

sll  $10, $8,   8   # 0x345678
srl  $10, $10, 24   # 0x34

andi $11,$8, 0xFF00
srl  $11, $11,    8   # 0x56

andi $12,$8, 0x00FF   # 0101 0110 0111 1000
                      # 0000 0000 1111 1111__
                      # 0000 0000 0111 1000    0x78